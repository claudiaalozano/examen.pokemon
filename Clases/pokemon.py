#funcion para id del pokemón, que es un número entero y único que identifica cada pokemon
def id_pokemon(nombre):
    if nombre == "Pikachu":
        return 1
    elif nombre == "Pidgey":
        return 2
    elif nombre == "Squirtle":
        return 3
    elif nombre == "Diglett":
        return 4
    elif nombre == "Venusaur":
        return 5
    elif nombre == "Charmeleon":
        return 6
    
